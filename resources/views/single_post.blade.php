            		Abdul Matin Chowdhury (Bengali: আব্দুল মতিন চৌধুরী; 1915 – 19 February 1990), popularly known as Shaikh-e-Fulbari (Shaikh of Fulbari), was a Bengali religious scholar and political activist. A disciple of Hussain Ahmed Madani, Chowdhury later amassed an extensive religious following in his own right and over the course of the 20th century, became involved in the various political upheavals in the Indian subcontinent.

<br/>
<h4>Early life and background</h4>

Abdul Matin Chowdhury was born in 1915 to a Zamindar family in the village of Fulbari, Golapganj, in what is today Sylhet, Bangladesh. He was the youngest of the six children of his parents, Rizwan Uddin and Khair-un-nessa Chowdhury. Through his father, he claimed descent from Abdullah ibn Ja'far, a nephew of the fourth Rashidun caliph, Ali ibn Abi Talib.

After being orphaned at the age of eight, Chowdhury initially lived with his elder sister, before coming under the care of his maternal uncle in the village of Ronokali.[3] In 1928, he was introduced to the eminent Islamic scholar Hussain Ahmed Madani, who had been familiar with his late father. Having impressed Madani with his religious piety and enthusiasm, Chowdhury returned with him to Uttar Pradesh, where he became enrolled at Darul Uloom Deoband, a prominent Islamic university. During this time, he developed a close relationship with Madani, whose home he stayed at during his education, later becoming his religious disciple. Chowdhury graduated from the university after nine years, having had proven a competency in Hadiths, Tafsir and Arabic literature among others.
<br/>
<h4>Political and religious career</h4>

Under Madani's instructions, he took part in the Indian independence movement, becoming a leader among groups seeking to cast off British control. Following the Partition of India in 1947, Chowdhury supported the development of the newly created Pakistan, working alongside the leadership of the Jamiat Ulema-e-Hind organisation. When Bangladesh declared independence in 1971, he attempted to prevent incidents of violence against the populous by the Pakistani military. When fifty of his relatives in Ronokali were cornered by the army, Chowdhury successfully appealed to the soldiers to spare them. On another occasion, he was able to intervene to save the lives of seven Hindus under similar circumstances. Chowdhury subsequently played an active part in the Bangladesh Khilafat movement, working under the leadership of Hafezzi Huzur and presiding over many of its conferences.

In the period of 1966–67, Chowdhury undertook extensive missionary work as part of the Tablighi Jamaat organisation. His travels included parts of Europe such as Turkey, the United Kingdom and Yugoslavia, as well as countries in Asia like Iran, Saudi Arabia and Japan. He gained a reputation as a skilled and passionate orator, being able to give lectures in multiple languages due to his fluency in Bengali, Urdu, Arabic and English. Chowdhury was further recognised both as a philanthropist, due to his support of the poor, and as a writer, receiving praise for his publication, Shatter Mapkati (Scale of Truth).He eventually built up an extensive religious following, with his disciples and pupils including notable figures such as Emdadul Haque, Nur Uddin Gouharpuri and Amin Uddin Shaikh-e-Ketia.
<br/>
<h4>Death and legacy</h4>

Chowdhury died of illness on 19 February 1990 and was survived by his wife and seven children. His burial in his family's ancestral cemetery at Fulbari was a public event and was attended by thousands of his followers. A welfare organisation called the Allama Abdul Motin Chowdhury Shaikh-e-Fulbari Foundation was later established in his honour by a group of his former disciples.
